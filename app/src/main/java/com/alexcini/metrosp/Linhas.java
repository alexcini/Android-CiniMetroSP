public class Linhas {
    private final String corLinha;
    private final String numLinha;
    public final String imgLinha;

    public Linhas(String corLinha, String numLinha,
                  String imgLinha) {
        this.corLinha = corLinha;
        this.numLinha = numLinha;
        this.imgLinha = imgLinha;
    }

    public String getCorLinha() {
        return corLinha;
    }
    public String getNumLinha() {
        return corLinha;
    }
    public String getImgLinha() {
        return corLinha;
    }
}
